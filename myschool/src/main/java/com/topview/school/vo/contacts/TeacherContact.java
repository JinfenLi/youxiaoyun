public class TeacherContact {

	private String id; // 老师id
	private String name; // 老师姓名
	private String leadingClassId; // 所带班级id，只有班主任该属性才不为空
	private boolean isHeadTeacher; // 是否班主任
	private String sex; // 性别
	private String picUrl; // 头像Url
	private String lastUpdate; // 最后更新时间
	private String phone; // 老师电话
	private List<String> subject; // 所教学科
	private List<String> department; // 所属部门名称，如语文组、行政组
	private List<String> position; // 职位如校长、主任等

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLeadingClassId() {
		return leadingClassId;
	}

	public void setLeadingClassId(String leadingClassId) {
		this.leadingClassId = leadingClassId;
	}

	public boolean isHeadTeacher() {
		return isHeadTeacher;
	}

	public void setHeadTeacher(boolean isHeadTeacher) {
		this.isHeadTeacher = isHeadTeacher;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getPicUrl() {
		return picUrl;
	}

	public void setPicUrl(String picUrl) {
		this.picUrl = picUrl;
	}

	public String getLastUpdate() {
		return lastUpdate;
	}

	public void setLastUpdate(String lastUpdate) {
		this.lastUpdate = lastUpdate;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public List<String> getSubject() {
		return subject;
	}

	public void setSubject(List<String> subject) {
		this.subject = subject;
	}

	public List<String> getDepartment() {
		return department;
	}

	public void setDepartment(List<String> department) {
		this.department = department;
	}

	public List<String> getPosition() {
		return position;
	}

	public void setPosition(List<String> position) {
		this.position = position;
	}

}
