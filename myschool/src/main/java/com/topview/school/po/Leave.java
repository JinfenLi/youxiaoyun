package com.topview.school.po;

import java.util.Date;

/**
 * 请假申请PO
 */
public class Leave {

	private String id;
	private String type; // 请假类型
	private String reason; // 请假原因
	private Date applyTime; // 申请时间
	private Integer status; // 状态，1为未审核，2为通过，3为拒绝
	private String tScParentId; // 申请家长的id
	private String tScTeacherId; // 审批教师的id
	private String rejectReason; // 拒绝原因
	private String studentId; // 学生id
	private int accessoryCount; // 附件数量

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getReason() {
		return reason;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}

	public Date getApplyTime() {
		return applyTime;
	}

	public void setApplyTime(Date applyTime) {
		this.applyTime = applyTime;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public String gettScParentId() {
		return tScParentId;
	}

	public void settScParentId(String tScParentId) {
		this.tScParentId = tScParentId;
	}

	public String gettScTeacherId() {
		return tScTeacherId;
	}

	public void settScTeacherId(String tScTeacherId) {
		this.tScTeacherId = tScTeacherId;
	}

	public String getRejectReason() {
		return rejectReason;
	}

	public void setRejectReason(String rejectReason) {
		this.rejectReason = rejectReason;
	}

	public String getStudentId() {
		return studentId;
	}

	public void setStudentId(String studentId) {
		this.studentId = studentId;
	}

	public int getAccessoryCount() {
		return accessoryCount;
	}

	public void setAccessoryCount(int accessoryCount) {
		this.accessoryCount = accessoryCount;
	}

}
