package com.topview.school.util;

public class HtmlUtil {
	/**
	 * 将文本转化成HTML
	 * 
	 * @param text
	 * @return
	 */
	public static String toHtml(String text) {
		String htmlHead = "<!doctype html><html><head><meta name=\"viewport\" content=\"width=device-width, initial-scale=1\"><meta charset=\"utf-8\"><link rel=\"stylesheet\" href=\"../resources/css/bootstrap.min.css\"> </head><body><div class=\"container\"> <div class=\"row\"><div class=\"col-xs-12\">";
		String htmlBody = "</div></div></div></body></html>";
		return htmlHead + text + htmlBody;
	}
}
