public class HistoryMessageVo {

	private String content; // 短信内容
	private String sendTime; // 发送时间
	private List<String> receiversId; // 接收者id列表
	private List<String> receiversName; //接收者姓名

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getSendTime() {
		return sendTime;
	}

	public void setSendTime(String sendTime) {
		this.sendTime = sendTime;
	}

	public List<String> getReceiversId() {
		return receiversId;
	}

	public void setReceiversId(List<String> receiversId) {
		this.receiversId = receiversId;
	}

	public List<String> getReceiversName() {
		return receiversName;
	}

	public void setReceiversName(List<String> receiversName) {
		this.receiversName = receiversName;
	}

}
