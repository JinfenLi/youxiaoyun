package com.topview.school.po;

public class ShowFeatures {
		private int id;
		private String userId;
		private int status;
		@Override
		public String toString() {
			return "ShowFeatures [id=" + id + ", userId=" + userId
					+ ", status=" + status + "]";
		}
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public String getUserId() {
			return userId;
		}
		public void setUserId(String userId) {
			this.userId = userId;
		}
		public int getStatus() {
			return status;
		}
		public void setStatus(int status) {
			this.status = status;
		}
		
}	
