package com.topview.school.po;

import java.util.Date;

public class Teacher {

	private String id;
    private String tScClassId; //班主任对应班级id，只有是班主任该字段才有对应班级id
    private String tScGradeId; //级长对应年级id，只有级长该字段才有对应年级id
    private String tScSchoolId; //所属学校id
    private String name; //教师姓名
    private String sex; //教师性别
    private String email; //教师邮箱地址
    private String phone; //教师手机号码
    private Date createTime; //创建时间
    private String password; //密码
    private String picUrl; //头像Url
    private Date lastUpdate; //头像最后更新时间
    private String education; //学历
    private String idcard; //工号
    private Date birthday; //生日
    private String schoolName;  //学校名字 

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String gettScClassId() {
        return tScClassId;
    }

    public void settScClassId(String tScClassId) {
        this.tScClassId = tScClassId;
    }

    public String gettScGradeId() {
        return tScGradeId;
    }

    public void settScGradeId(String tScGradeId) {
        this.tScGradeId = tScGradeId;
    }

    public String gettScSchoolId() {
        return tScSchoolId;
    }

    public void settScSchoolId(String tScSchoolId) {
        this.tScSchoolId = tScSchoolId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

	public String getPicUrl() {
		return picUrl;
	}

	public void setPicUrl(String picUrl) {
		this.picUrl = picUrl;
	}

	public Date getLastUpdate() {
		return lastUpdate;
	}

	public void setLastUpdate(Date lastUpdate) {
		this.lastUpdate = lastUpdate;
	}

	public String getEducation() {
		return education;
	}

	public void setEducation(String education) {
		this.education = education;
	}

	public String getIdcard() {
		return idcard;
	}

	public void setIdcard(String idcard) {
		this.idcard = idcard;
	}

	public Date getBirthday() {
		return birthday;
	}

	public void setBirthday(Date birthday) {
		this.birthday = birthday;
	}

	public String getSchoolName() {
		return schoolName;
	}

	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}

    
}