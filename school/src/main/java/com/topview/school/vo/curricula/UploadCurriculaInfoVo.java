package com.topview.school.vo.curricula;

/**
 * @Description 上传课表解析课程表所用VO
 */
public class UploadCurriculaInfoVo {

	private String curriculaVariableId; //选课id
	private String curriculaId; //课程id
	private String clazzId; //班级id
	private String semesterId; //学期id
	private String subjectName; //学科名称
	private String curriculaName; //课程名称
	
	public String getCurriculaVariableId() {
		return curriculaVariableId;
	}
	public void setCurriculaVariableId(String curriculaVariableId) {
		this.curriculaVariableId = curriculaVariableId;
	}
	public String getCurriculaId() {
		return curriculaId;
	}
	public void setCurriculaId(String curriculaId) {
		this.curriculaId = curriculaId;
	}
	public String getClazzId() {
		return clazzId;
	}
	public void setClazzId(String clazzId) {
		this.clazzId = clazzId;
	}
	public String getSemesterId() {
		return semesterId;
	}
	public void setSemesterId(String semesterId) {
		this.semesterId = semesterId;
	}
	public String getSubjectName() {
		return subjectName;
	}
	public void setSubjectName(String subjectName) {
		this.subjectName = subjectName;
	}
	public String getCurriculaName() {
		return curriculaName;
	}
	public void setCurriculaName(String curriculaName) {
		this.curriculaName = curriculaName;
	}
	
}
