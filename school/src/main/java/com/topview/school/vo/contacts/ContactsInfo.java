package com.topview.school.vo.contacts;

import java.util.List;

public class ContactsInfo {

	private String id; //家长或老师id
	private String name; //家长或老师姓名
	private String classId; //如果是班主任对应的班级id
	private boolean isHeadTeacher; //是否班主任
	private List<String> teacherClass; //所教班级名称
	private String studentName; //若是家长则同时返回学生姓名,若是老师则过滤
	private String sex; //性别
	private String phone; //家长或老师电话
	private String subject; //课程名
	private String picUrl; //头像Url
	private String lastUpdate; //最后更新时间
	private String department; //所属部门名称，如语文组、行政组
	private String departmentId; //所属部门对应的id 
	private List<String> position; //职位如校长、主任等
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getLastUpdate() {
		return lastUpdate;
	}
	public void setLastUpdate(String lastUpdate) {
		this.lastUpdate = lastUpdate;
	}
	public String getName() {
		return name;
	}
	public String getClassId() {
		return classId;
	}
	public void setClassId(String classId) {
		this.classId = classId;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStudentName() {
		return studentName;
	}
	public boolean isHeadTeacher() {
		return isHeadTeacher;
	}
	public void setHeadTeacher(boolean isHeadTeacher) {
		this.isHeadTeacher = isHeadTeacher;
	}
	public List<String> getTeacherClass() {
		return teacherClass;
	}
	public void setTeacherClass(List<String> teacherClass) {
		this.teacherClass = teacherClass;
	}
	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getPicUrl() {
		return picUrl;
	}
	public void setPicUrl(String picUrl) {
		this.picUrl = picUrl;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public List<String> getPosition() {
		return position;
	}
	public void setPosition(List<String> position) {
		this.position = position;
	}
	public String getDepartmentId() {
		return departmentId;
	}
	public void setDepartmentId(String departmentId) {
		this.departmentId = departmentId;
	}
	
}
