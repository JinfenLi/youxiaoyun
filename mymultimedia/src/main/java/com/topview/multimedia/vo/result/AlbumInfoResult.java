package com.topview.multimedia.vo.result;

import java.util.List;

import com.topview.multimedia.vo.AlbumInfo;

public class AlbumInfoResult {
	private int code;
	private boolean success;
	private List<AlbumInfo> infoResult;

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public boolean isSuccess() {
		return success;
	}

	public void setSuccess(boolean success) {
		this.success = success;
	}

	public List<AlbumInfo> getInfoResult() {
		return infoResult;
	}

	public void setInfoResult(List<AlbumInfo> infoResult) {
		this.infoResult = infoResult;
	}



}
