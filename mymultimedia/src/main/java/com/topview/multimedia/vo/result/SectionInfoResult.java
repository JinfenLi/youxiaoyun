package com.topview.multimedia.vo.result;

import java.util.List;

import com.topview.multimedia.vo.SectionInfo;

public class SectionInfoResult {
	private int code;
	private boolean success;
	private List<SectionInfo> result;

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public boolean isSuccess() {
		return success;
	}

	public void setSuccess(boolean success) {
		this.success = success;
	}

	public List<SectionInfo> getResult() {
		return result;
	}

	public void setResult(List<SectionInfo> result) {
		this.result = result;
	}

}
