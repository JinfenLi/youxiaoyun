package com.topview.multimedia.vo.result;

import java.util.List;

import com.topview.multimedia.vo.PraiseVo;

public class PraiseVoResult {

	private int code;
	private boolean success;
	private List<PraiseVo> result;

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public boolean isSuccess() {
		return success;
	}

	public void setSuccess(boolean success) {
		this.success = success;
	}

	public List<PraiseVo> getResult() {
		return result;
	}

	public void setResult(List<PraiseVo> result) {
		this.result = result;
	}

}
